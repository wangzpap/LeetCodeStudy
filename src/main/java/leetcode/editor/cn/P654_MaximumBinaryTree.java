class Solution {
    public TreeNode constructMaximumBinaryTree(int[] nums) {
		return BuildMaxTree(nums,0,nums.length-1); // æ³¨æ„æ­¤å¤„ï¼Œä¸ºnums.length-1
    }

	/**
	 * æ³¨æ„æ­¤å‡½æ•°æ„å»ºçš„å‚æ•° æ·»åŠ startå’Œendï¼Œè€Œä¸æ˜¯åªæœ‰nums
	 * @param nums
	 * @param start
	 * @param end
	 * @return
	 */
	public TreeNode BuildMaxTree(int[] nums,int start,int end) {
		if(nums.length==0)return null;
		if(start>end)return null;

		// å¯»æ‰¾[start,end]åŒºé—´æœ€å¤§å€¼åŠç´¢å¼•
		int maxindex = -1;
		int maxNum = Integer.MIN_VALUE;
		for (int i = start; i <= end; i++) { // æ³¨æ„æ­¤å¤„i <= end
			int x = nums[i];
			if(x>maxNum){
				maxindex = i;
				maxNum = x;
			}
		}
		TreeNode treeNode = new TreeNode(maxNum);
		// å·¦å³å­æ ‘é€’å½’
		TreeNode left = BuildMaxTree(nums,start,maxindex-1);
		TreeNode right = BuildMaxTree(nums,maxindex+1,end);
		treeNode.left = left;
		treeNode.right = right;
		return treeNode;
	}
}
